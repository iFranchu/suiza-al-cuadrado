class Calculadora {
    sumar(num1,num2){return num1 + num2}
    restar(num1,num2){return num1 - num2}
    multiplicar(num1,num2){return num1 * num2}
    dividir(num1,num2){return num1 / num2}
    coseno(num){return Math.cos(num)}
    seno(num){return Math.sin(num)}
    tangente(num){return Math.tan(num)}
    arcocoseno(num){return Math.acos(num)}
    arcoseno(num){return Math.asin(num)}
    arcotangente(num){return Math.atan(num)}
}